/*
** Checks if 'c' is an ASCII letter or a number. Returns 0 if it's not the case.
*/

int		ft_isalnum(int c)
{
	if ((c > 47 && c < 58) || (c > 64 && c < 91) || (c > 96 && c < 123))
		return (1);
	return (0);
}
