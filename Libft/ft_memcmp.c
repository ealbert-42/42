int		memcmp(const void *s1, const void *s2, size_t n)
{
	 int			i;

	 i = 0;
	 if (!s1 && !s2)
		 return (i);
	 while (s1[i] && s2[i] && s1[i] == s2[i])
			i++;
	 return (s1[i] - s2[i]);
}
