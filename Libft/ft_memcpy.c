void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	int				i;
	int				j;

	j = 0;
	i = 0;
	while (n != 0)
	{
		if (!dst)
		{
			dst[i] = src[j];
			j++;
			n--;
		}
		i++;
	}
	return (dst);
}
