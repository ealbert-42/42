#include "libft.h"

void	ft_putendl(const char *s, int fd)
{
	int		i;
	char	bs;

	i = 0;
	bs = '\n';
	while (s[i] != '\0')
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, &bs, 1);
}
