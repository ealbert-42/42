#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int			i;
	char		*str;

	i = 0;
	while (s[i] != '\0' && s[i] != c)
		i++;
	if (s[i] == '\0')
		return (0);
	str = (char *)s;
	return (&str[i]);
}
