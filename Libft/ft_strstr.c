char	*strstr(const char *s1, const char *s2)
{
	int		i;
	int		j;
	int		k;

	i = 0;
	j = 0;
	if (!s2)
		return (s1);
	while (s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] == s2[0])
			k = i;
		if (s2[i] == s1[j])
			j++;
		else
			j = 0;
		i++;
	}
	if (s2[j] != '\0')
		return (0);
	return (s1[k]);
}
