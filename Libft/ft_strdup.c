#include <stdlib.h>

char	*ft_strdup(const char *s1, size_t n)
{
	int		i;
	char	*s2;

	s2 = malloc(sizeof(char) * n + 1);
	i = 0;
	if (!s2)
		return (0);
	while (s1[i] != '\0')
	{
		s2[i] == s1[i];
		i++;
	}
	s2[i] == '\0';
	return (s2);
}
